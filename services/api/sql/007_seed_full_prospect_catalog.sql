-- Ensure broad test availability so scheduling can work with any seeded winery.
with seeded_wineries as (
  select winery_id
  from winery
  where active = true
),
days as (
  select generate_series(current_date, current_date + interval '365 day', interval '1 day')::date as service_date
),
slots as (
  select *
  from (values
    ('09:00'::time, '10:15'::time),
    ('10:30'::time, '11:45'::time),
    ('12:00'::time, '13:15'::time),
    ('13:30'::time, '14:45'::time),
    ('15:00'::time, '16:15'::time)
  ) as s(start_time, end_time)
),
expanded as (
  select
    (
      substr(md5(sw.winery_id::text || d.service_date::text || s.start_time::text), 1, 8) || '-' ||
      substr(md5(sw.winery_id::text || d.service_date::text || s.start_time::text), 9, 4) || '-' ||
      substr(md5(sw.winery_id::text || d.service_date::text || s.start_time::text), 13, 4) || '-' ||
      substr(md5(sw.winery_id::text || d.service_date::text || s.start_time::text), 17, 4) || '-' ||
      substr(md5(sw.winery_id::text || d.service_date::text || s.start_time::text), 21, 12)
    )::uuid as availability_id,
    sw.winery_id,
    d.service_date,
    s.start_time,
    s.end_time
  from seeded_wineries sw
  cross join days d
  cross join slots s
)
insert into winery_availability (availability_id, winery_id, service_date, start_time, end_time, remaining_capacity, status)
select
  availability_id,
  winery_id,
  service_date,
  start_time,
  end_time,
  24 as remaining_capacity,
  'open' as status
from expanded
on conflict (availability_id) do update
set
  winery_id = excluded.winery_id,
  service_date = excluded.service_date,
  start_time = excluded.start_time,
  end_time = excluded.end_time,
  remaining_capacity = excluded.remaining_capacity,
  status = excluded.status;
